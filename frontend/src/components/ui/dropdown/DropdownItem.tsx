import type React from 'react'
import Link from 'next/link'
import { cn } from '@/lib/utils'

interface DropdownItemProps {
  tag?: 'a' | 'button'
  href?: string
  onClick?: () => void
  onItemClick?: () => void
  baseClassName?: string
  className?: string
  children: React.ReactNode
}

/**
 * DropdownItem Component - Item trong dropdown menu
 */
export const DropdownItem: React.FC<DropdownItemProps> = ({
  tag = 'button',
  href,
  onClick,
  onItemClick,
  baseClassName = 'block w-full text-left px-4 py-2 text-sm text-gray-700 hover:bg-gray-100 hover:text-gray-900 dark:text-gray-300 dark:hover:bg-gray-800',
  className = '',
  children,
}) => {
  const combinedClasses = cn(baseClassName, className)

  const handleClick = (event: React.MouseEvent) => {
    if (tag === 'button') {
      event.preventDefault()
    }
    if (onClick) onClick()
    if (onItemClick) onItemClick()
  }

  if (tag === 'a' && href) {
    return (
      <Link href={href} className={combinedClasses} onClick={handleClick}>
        {children}
      </Link>
    )
  }

  return (
    <button onClick={handleClick} className={combinedClasses}>
      {children}
    </button>
  )
}


